    classes.push(
      "fill-neutral-Gray-9",
      "group-hover:fill-neutral-Gray-11",
      "group-focus:fill-neutral-Gray-13",
      "group-disabled:fill-neutral-Gray-6",
    );
  }

  return classes;
});
